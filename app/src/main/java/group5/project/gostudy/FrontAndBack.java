package group5.project.gostudy;

public class FrontAndBack
{
	private String question;
	private String answer;
	
	public FrontAndBack(String q, String a)
	{
		question = q;
		answer = a;
	}
	public String getQuestion()
	{
		return question;
	}
	
	public String getAnswer()
	{
		return answer;
	}
}
